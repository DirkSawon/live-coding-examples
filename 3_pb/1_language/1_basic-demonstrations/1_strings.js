// Quotes
    // Single quotes: ' 
    // Ein typischer string mit single quotes (hochkomma)
    'This is a string';

    // Double quotes: "
    // Ein typischer string mit double quotes(gänsefüßchen)
    "This is a string";

    // Eine single quote innerhalb eines double-quote strings:
    "It's a string";

    // Ein single quote innerhalb eines single quote strings:
    'It\'s a String';
    
    // Eine double quote innerhalb eines single quote strings:
    'This is an "awesome" string';

    // Ein double quote innerhalb eines double-quote strings:
    "This is an \"awesome\" string";

// Concatenation
    // mehrere strings zusammenfügen, um einen satz zu erstellen.
    console.log('Hallo' + ' ' + 'Welt');

    // zwei strings mit concatenation zusammenfügen
    console.log('Hello '.concat('World'));

    // mehrere strings innerhalb der concat methode hinzufügen
    console.log('Hello'.concat(' ', 'World'));

